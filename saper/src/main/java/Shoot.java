public class Shoot {
    Integer CoordinateShootX;
    Integer CoordinateShootY;

    public Integer getCoordinateShootX() {
        return CoordinateShootX;
    }

    public Integer getCoordinateShootY() {
        return CoordinateShootY;
    }

    public Shoot(Integer coordinateShootX, Integer coordinateShootY) {
        CoordinateShootX = coordinateShootX;
        CoordinateShootY = coordinateShootY;
    }
}
